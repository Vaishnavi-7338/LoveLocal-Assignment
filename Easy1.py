# Define a string variable 's'
s = "luffy is still joyboy"

# Use the split() method to split the string into a list of words
g = s.split()

# Access the last element of the list using index -1
last_word = g[-1]

# Use the len() function to get the length of the last word
length_of_last_word = len(last_word)

# Print the result
print(length_of_last_word)
